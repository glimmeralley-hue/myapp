fun main() {
    //if... else if...else conditional statement
    // This is used to evaluate multiple conditions .The else part gets executed when all the conditions given evaluate to false
     var number = 0

     if (number >0){
        println("the number is positive")

     }
     else if (number==0) {
        println("the number is zero")

     }
     else{
        println("the number is negative")
     }
    
}